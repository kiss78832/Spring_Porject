package com.spring.function.model;

public class FunctionVO {
	
	private String fun_num;
	private String fun_name;
	private String fun_url;
	
	
	public String getFun_num() {
		return fun_num;
	}
	public void setFun_num(String fun_num) {
		this.fun_num = fun_num;
	}
	public String getFun_name() {
		return fun_name;
	}
	public void setFun_name(String fun_name) {
		this.fun_name = fun_name;
	}
	public String getFun_url() {
		return fun_url;
	}
	public void setFun_url(String fun_url) {
		this.fun_url = fun_url;
	}

	
	
}
